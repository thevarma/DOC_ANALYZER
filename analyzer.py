# LLM-based analysis using LangChain + Gemini

def analyze_doc(url):
    return {
        'url': url,
        'readability': 'Good',
        'structure': 'Needs better headings',
        'completeness': 'Add more examples',
        'style': 'Too formal',
        'suggestions': ['Use simpler language.', 'Add examples in section 2.']
    }